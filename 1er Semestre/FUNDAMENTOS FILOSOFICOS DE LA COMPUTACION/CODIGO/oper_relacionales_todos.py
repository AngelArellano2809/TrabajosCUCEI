#Programa para usar todos los operadores de relación sobre dos números
#Entrada:
n1 = int(input("Dame un número:"))
n2 = int(input("Dame otro número:"))
if n1 == n2:
    print(n1, "es igual que", n2)
if n1 != n2:
    print(n1, "no es igual a", n2)
if n1 > n2:
    print(n1, "es mayor que", n2)
if n1 < n2:
    print(n1, "es menor que", n2)
if n1>= n2:
    print(n1, "es mayor o igual que", n2)
if n1<= n2:
    print(n1, "es menor o igual que", n2)
