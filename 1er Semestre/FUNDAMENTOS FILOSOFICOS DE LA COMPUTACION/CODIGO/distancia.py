#Programa para calcular la distancia recorrida por un auto,
#solicitando los datos de velocidad y tiempo
#Entrada:
v = int (input("Teclea la velocidad del auto"))
t = int (input("Teclea el tiempo recorrido del auto"))
#Proceso
d = v * t
#salida
print("La distancia recorrida por el auto fue",d,"Kilometros")
